package com.aila.model;

public class DeepAnalysis {
	
	private int deep_idx;
	private String deep_result;
	private int deep_date;
	
	
	public int getDeep_idx() {
		return deep_idx;
	}
	public void setDeep_idx(int deep_idx) {
		this.deep_idx = deep_idx;
	}
	public String getDeep_result() {
		return deep_result;
	}
	public void setDeep_result(String deep_result) {
		this.deep_result = deep_result;
	}
	public int getDeep_date() {
		return deep_date;
	}
	public void setDeep_date(int deep_date) {
		this.deep_date = deep_date;
	}
	
	
	
}
