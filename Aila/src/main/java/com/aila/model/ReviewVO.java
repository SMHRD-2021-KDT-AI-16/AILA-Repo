package com.aila.model;

public class ReviewVO {
	
	private String review_idx;
	private String review_content;
	private String review_source;
	private int review_date;
	private int review_rating;
	
	
	
	public String getReview_idx() {
		return review_idx;
	}
	public void setReview_idx(String review_idx) {
		this.review_idx = review_idx;
	}
	public String getReview_content() {
		return review_content;
	}
	public void setReview_content(String review_content) {
		this.review_content = review_content;
	}
	public String getReview_source() {
		return review_source;
	}
	public void setReview_source(String review_source) {
		this.review_source = review_source;
	}
	public int getReview_date() {
		return review_date;
	}
	public void setReview_date(int review_date) {
		this.review_date = review_date;
	}
	public int getReview_rating() {
		return review_rating;
	}
	public void setReview_rating(int review_rating) {
		this.review_rating = review_rating;
	}
	
	
}
